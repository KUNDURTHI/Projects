package nullpointer;

public class Honada {

	private String crv;
	private String civic;
	private String accord;
	/**
	 * @return the crv
	 */
	public String getCrv() {
		return crv;
	}
	/**
	 * @param crv the crv to set
	 */
	public void setCrv(String crv) {
		this.crv = crv;
	}
	/**
	 * @return the civic
	 */
	public String getCivic() {
		return civic;
	}
	/**
	 * @param civic the civic to set
	 */
	public void setCivic(String civic) {
		this.civic = civic;
	}
	/**
	 * @return the accord
	 */
	public String getAccord() {
		return accord;
	}
	/**
	 * @param accord the accord to set
	 */
	public void setAccord(String accord) {
		this.accord = accord;
	}
	
}
